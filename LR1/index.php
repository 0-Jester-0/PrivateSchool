                <p> По данным UNESCO, ежегодно на обучение в Швейцарию приезжает порядка тысячи российских студентов </p>
            </div>
            <div class="main-state">
                <p>
                    Знаменитая своим политическим нейтралитетом, высоким качеством жизни и прекрасной альпийской природой,
                    Швейцария привлекает не только родителей, которые отправляют детей в знаменитые на весь мир швейцарские школы-пансионы,
                    но и выдающихся ученых и преподавателей со всех концов света. Исключительная оснащенность и «звездный» преподавательский
                    состав ставят швейцарские учебные заведения в число лучших в мире.
                </p>
                <br>
                <p>
                    Государственные вузы Швейцарии занимают топовые позиции в списках лучших научно-исследовательских центов мира. Среди
                    технических направлений, которые стоит изучать в Швейцарии – физика, архитектура, география, инженерия, естественные науки,
                    информатика, компьютерные науки.
                </p>
                <br>
                <p>
                    Два самых распространённых направления в частных вузах – бизнес и гостеприимство.
                </p>
                <br>
                <p>
                    Большинство швейцарских бизнес-школ частные. В отличие от российских, они обучают как по программам МВА, так и бакалавриата и
                    магистратуры. Одна из самых популярных программ в бизнес-школах – International Management – сочетает изучение бизнеса и
                    международных отношений, что не удивительно, ведь в Швейцарии расположены штаб-квартиры ООН, ВОЗ, Красного Креста, ЮНЕСКО и многих
                    других международных организаций. Студенты бизнес-школ получают возможность пройти в них стажировку или устроиться на работу.
                </p>
                <br>
                <p>
                    Наконец, Швейцария – родина индустрии гостеприимства. Здесь располагаются элитные университеты, обучающие отельному и ресторанному
                    менеджменту, организации мероприятий (в том числе, масштаба чемпионатов мира по футболу), управлению люксовыми брендами и другим
                    направлениям клиентского сервиса.
                </p>
