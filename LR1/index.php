                        <h5 class="red-title">Швейцария задает тренды в школьном образовании.</h5>
                        <br>
                        <p>
                            Здесь родился и работал основоположник теории когнитивного развития Жан Пьяже; именно в Швейцарии в 1950-х годах появилась
                            одна из самых престижных и уважаемых во всем мире программ среднего образования – Международный Бакалавриат (IB).
                        </p>
                    </div>
                    <div class="col-sm-4">
                        <img class="swiss" src="images/swiss.png">
                        <h5 class="red-title">Страна активно инвестирует в образование и науку.</h5>
                        <br>
                        <p>
                            С 2021 по 2025 гг. швейцарское правительство планирует потратить на поддержку образования, исследовательской деятельности
                            и инноваций 28,1 млрд франков (25,9 млрд евро).
                        </p>
                    </div>
                    <div class="col-sm-4">
                        <img class="swiss" src="images/swiss.png">
                        <h5 class="red-title">В Швейцарии всего 12 государственных вузов.</h5>
                        <br>
                        <p>
                            11 из них входят в топ-500, а 5 – в топ-100 лучших университетов мира. Две федеральные технические школы – ETH Zurich и
                            EPF Lausanne – занимают 8 и 14 строчки, соответственно.
                        </p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-sm-4">
                        <img class="swiss" src="images/swiss.png">
                        <h5 class="red-title">Обучение в государственных вузах Швейцарии стоит всего около 1500 франков в год (1390 евро).</h5>
                        <br>
                        <p>
                            Правда, образование в этой стране все равно доступно далеко не всем: из-за высоких цен на проживание общие расходы
                            окажутся не ниже 15 000 франков в год.
                        </p>
                    </div>
                    <div class="col-sm-4">
                        <img class="swiss" src="images/swiss.png">
                        <h5 class="red-title">4 из 5 лучших школ гостеприимства в мире находятся в Швейцарии, все они частные.</h5>
                        <br>
                        <p>
                            Стоимость обучения в самых престижных из них может доходить до 55 000 швейцарских франков за год (50 000 евро).
                            В числе студентов школ гостеприимства – дети элиты со всех уголков Земли.
                        </p>
                    </div>
                    <div class="col-sm-4">
                        <img class="swiss" src="images/swiss.png">
                        <h5 class="red-title">В Швейцарии очень развита культура предпринимательства.</h5>
                        <br>
                        <p>
                            Многие выпускники с бизнес-дипломом основывают собственные стартапы. Для поддержки предпринимательства в стране
                            действует множество инкубаторов.
                        </p>
                    </div>
                </div>
                <p style="margin-top: 100px">
                    В спецпроекте «Образование в Швейцарии» Forbes Education расскажет обо всем, что важно знать о школах-пансионах, государственных
                    и частных вузах Швейцарии, самых востребованных направлениях обучения, вступительных требованиях и жизни в этой стране.
                </p>
                <br>
                <p>
                    В этом разделе вы найдете обзоры учебных заведений, личные истории выпускников, советы по выбору школы или университета,
                    рекомендации по поступлению на программы бакалавриата, магистратуры и PhD, информацию о стипендиях и визовых особенностях Швейцарии.
